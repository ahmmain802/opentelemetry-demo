# Publish verification results
PUBLISH_URL="$PACT_BROKER_URL/pacts/provider/$PROVIDER_NAME/consumer/$CONSUMER_NAME/pact-version/$PACT_VERSION_HASH/verification-results"

VERIFICATION_RESPONSE=$(curl -s -w "%{http_code}" -X POST \
    "$PUBLISH_URL" \
    -H "Content-Type: application/json" \
    -d "$VERIFICATION_RESULTS")

HTTP_CODE="${VERIFICATION_RESPONSE: -3}"
RESPONSE_BODY="${VERIFICATION_RESPONSE%???}"

if [ "$HTTP_CODE" = "201" ] || [ "$HTTP_CODE" = "200" ]; then
    echo -e "${GREEN}✅ Provider verification results published successfully!${NC}"
else
    echo -e "${RED}❌ Failed to publish verification results${NC}"
    echo "   HTTP Status: $HTTP_CODE"
    echo "   Response: $RESPONSE_BODY"
    exit 1
fi

echo -e "\n${BLUE}📋 Step 6: Contract Testing Summary${NC}"
echo "===================================="

# Get the matrix status
echo "📊 Fetching contract testing matrix..."

MATRIX_RESPONSE=$(curl -s "$PACT_BROKER_URL/matrix?q%5B%5D%5Bpacticipant%5D=$CONSUMER_NAME&q%5B%5D%5Bversion%5D=$CONSUMER_VERSION&q%5B%5D%5Bpacticipant%5D=$PROVIDER_NAME&q%5B%5D%5Bversion%5D=$PROVIDER_VERSION")

DEPLOYABLE=$(echo "$MATRIX_RESPONSE" | jq -r '.summary.deployable')
REASON=$(echo "$MATRIX_RESPONSE" | jq -r '.summary.reason')
SUCCESS_COUNT=$(echo "$MATRIX_RESPONSE" | jq -r '.summary.success')
FAILED_COUNT=$(echo "$MATRIX_RESPONSE" | jq -r '.summary.failed')

echo ""
echo "📈 CONTRACT TESTING RESULTS:"
echo "   Consumer: $CONSUMER_NAME v$CONSUMER_VERSION"
echo "   Provider: $PROVIDER_NAME v$PROVIDER_VERSION"
echo "   Deployable: $DEPLOYABLE"
echo "   Successful Verifications: $SUCCESS_COUNT"
echo "   Failed Verifications: $FAILED_COUNT"

if [ "$DEPLOYABLE" = "false" ]; then
    echo -e "   ${RED}❌ Reason: $REASON${NC}"
    echo ""
    echo -e "${YELLOW}📋 NEXT STEPS:${NC}"
    echo "   1. Review contract violations in the provider verification tests"
    echo "   2. Update shipping service to match consumer expectations:"
    echo "      - Add product_id field to CartItem struct"
    echo "      - Add full address fields (street_address, city, state, country)"
    echo "      - Add request validation for empty items and missing fields"
    echo "      - Return 400 status codes for validation errors"
    echo "   3. Re-run this script to verify improvements"
else
    echo -e "   ${GREEN}✅ All contract tests passed!${NC}"
fi

echo ""
echo -e "${BLUE}🌐 VIEW RESULTS:${NC}"
echo "   Pact Broker: $PACT_BROKER_URL"
echo "   Matrix View: $PACT_BROKER_URL/matrix"
echo "   Consumer: $PACT_BROKER_URL/pacticipants/$CONSUMER_NAME"
echo "   Provider: $PACT_BROKER_URL/pacticipants/$PROVIDER_NAME"

echo ""
echo -e "${BLUE}🎯 CONTRACT TESTING WORKFLOW COMPLETED!${NC}"
echo "=============================================="

if [ "$DEPLOYABLE" = "false" ]; then
    echo -e "${YELLOW}⚠️  Contract violations detected - see summary above${NC}"
    exit 1
else
    echo -e "${GREEN}✅ All contracts verified successfully!${NC}"
    exit 0
fi