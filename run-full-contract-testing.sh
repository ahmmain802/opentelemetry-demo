#!/bin/bash

# Complete Contract Testing Workflow
# This script runs the full contract testing pipeline:
# 1. Starts Pact Broker
# 2. Runs consumer tests (generates contracts)
# 3. Runs provider verification tests
# 4. Publishes contracts and verification results to broker
# 5. Shows final status

set -e

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Configuration
PACT_BROKER_URL="http://localhost:9292"
CONSUMER_NAME="frontend"
PROVIDER_NAME="shipping-service"
CONSUMER_VERSION="1.0.0"
PROVIDER_VERSION="1.0.0"

echo -e "${BLUE}🚀 Starting Complete Contract Testing Workflow${NC}"
echo "=================================================="

# Function to check if a service is running
check_service() {
    local url=$1
    local service_name=$2
    local max_attempts=30
    local attempt=1
    
    echo -e "${YELLOW}⏳ Waiting for $service_name to be ready...${NC}"
    
    while [ $attempt -le $max_attempts ]; do
        if curl -s "$url" > /dev/null 2>&1; then
            echo -e "${GREEN}✅ $service_name is ready!${NC}"
            return 0
        fi
        echo "   Attempt $attempt/$max_attempts - waiting..."
        sleep 2
        ((attempt++))
    done
    
    echo -e "${RED}❌ $service_name failed to start after $max_attempts attempts${NC}"
    return 1
}

# Function to cleanup on exit
cleanup() {
    echo -e "\n${YELLOW}🧹 Cleaning up...${NC}"
    # Stop any background processes if needed
    # docker-compose -f docker-compose.pact-broker.yml down > /dev/null 2>&1 || true
}

trap cleanup EXIT

echo -e "\n${BLUE}📋 Step 1: Starting Pact Broker${NC}"
echo "=================================="

# Check if Pact Broker is already running
if curl -s "$PACT_BROKER_URL" > /dev/null 2>&1; then
    echo -e "${GREEN}✅ Pact Broker is already running${NC}"
else
    echo "🐳 Starting Pact Broker with Docker Compose..."
    docker-compose -f docker-compose.pact-broker.yml up -d
    
    # Wait for Pact Broker to be ready
    check_service "$PACT_BROKER_URL" "Pact Broker"
fi

echo -e "\n${BLUE}📋 Step 2: Running Consumer Contract Tests${NC}"
echo "============================================="

echo "🧪 Running Frontend consumer tests..."
echo "   Note: Consumer tests generate the contract expectations"

# For now, we'll use the existing contract file since consumer tests aren't fully implemented
if [ -f "pacts/consumer-contracts/frontend-shipping-service.json" ]; then
    echo -e "${GREEN}✅ Consumer contract file found${NC}"
    echo "   File: pacts/consumer-contracts/frontend-shipping-service.json"
else
    echo -e "${RED}❌ Consumer contract file not found${NC}"
    echo "   Expected: pacts/consumer-contracts/frontend-shipping-service.json"
    echo "   This would normally be generated by running consumer tests"
    exit 1
fi

echo -e "\n${BLUE}📋 Step 3: Publishing Consumer Contracts${NC}"
echo "=========================================="

echo "📤 Publishing consumer contract to Pact Broker..."

PUBLISH_RESPONSE=$(curl -s -w "%{http_code}" -X PUT \
    "$PACT_BROKER_URL/pacts/provider/$PROVIDER_NAME/consumer/$CONSUMER_NAME/version/$CONSUMER_VERSION" \
    -H "Content-Type: application/json" \
    -d @pacts/consumer-contracts/frontend-shipping-service.json)

HTTP_CODE="${PUBLISH_RESPONSE: -3}"
RESPONSE_BODY="${PUBLISH_RESPONSE%???}"

if [ "$HTTP_CODE" = "200" ] || [ "$HTTP_CODE" = "201" ]; then
    echo -e "${GREEN}✅ Consumer contract published successfully!${NC}"
    echo "   Consumer: $CONSUMER_NAME v$CONSUMER_VERSION"
    echo "   Provider: $PROVIDER_NAME"
else
    echo -e "${RED}❌ Failed to publish consumer contract${NC}"
    echo "   HTTP Status: $HTTP_CODE"
    echo "   Response: $RESPONSE_BODY"
    exit 1
fi

echo -e "\n${BLUE}📋 Step 4: Running Provider Verification Tests${NC}"
echo "================================================="

echo "🧪 Running Shipping Service provider verification tests..."
echo "   This tests the actual service against consumer expectations"

# Run the provider verification tests
cd src/shipping
echo "   Running: cargo test test_full_provider_verification -- --ignored --nocapture"

# Capture the test output and exit code
if cargo test test_full_provider_verification -- --ignored --nocapture 2>&1; then
    PROVIDER_TESTS_PASSED=true
    echo -e "${GREEN}✅ Provider verification tests completed${NC}"
else
    PROVIDER_TESTS_PASSED=false
    echo -e "${YELLOW}⚠️  Provider verification tests completed with contract violations (expected)${NC}"
fi

cd ../..

echo -e "\n${BLUE}📋 Step 5: Publishing Provider Verification Results${NC}"
echo "===================================================="

echo "📤 Publishing provider verification results to Pact Broker..."

# Get the pact version hash from the broker
PACT_VERSION_HASH=$(curl -s "$PACT_BROKER_URL/pacts/provider/$PROVIDER_NAME/consumer/$CONSUMER_NAME/version/$CONSUMER_VERSION" | jq -r '._links."pb:pact-version".name')

if [ "$PACT_VERSION_HASH" = "null" ] || [ -z "$PACT_VERSION_HASH" ]; then
    echo -e "${RED}❌ Failed to get pact version hash${NC}"
    exit 1
fi

echo "   Pact Version Hash: $PACT_VERSION_HASH"

# Create verification results JSON based on our test results
VERIFICATION_RESULTS=$(cat <<EOF
